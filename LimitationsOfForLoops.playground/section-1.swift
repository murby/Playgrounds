import UIKit

var bottlesOfSoda = 99

for bottlesOfSoda; bottlesOfSoda >= 0; bottlesOfSoda-- {
    if bottlesOfSoda != 0 {
        println("\(bottlesOfSoda) bottles of soda on the wall")
    } else {
        println("No more bottles of soda")
    }
    
}